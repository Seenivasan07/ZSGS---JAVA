package arithmetic;
public class ArithmeticOperations
{
	public int add(int a, int b)
	{
		return a + b;
	}	

	public int subraction(int a , int b)
	{
		return a - b;
	}
	
	public int multiply(int a , int b)
	{
		return a*b;
	}
	
	public int division(int a, int b)
	{
		return a/b;
	}

	public int modulo(int a, int b)
	{
		return a%b;
	}
}